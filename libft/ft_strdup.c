#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dst;
	size_t	len;

	len = ft_strlen(s);
	dst = malloc(sizeof(char) * (len + 1));
	if (dst)
		ft_strlcpy(dst, s, (len + 1));
	return (dst);
}
